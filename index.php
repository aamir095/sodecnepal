              <p style="line-height: 24px; text-align:justify;">Development Communication Society, Nepal (SODEC – Nepal) is a non-governmental organization registered at District Administration Office in Kathmandu and is affiliated with Social Welfare Council since 2010. </p><br/>
			  <p style="line-height: 24px; text-align:justify;">The organization was founded by a group of mass communication professionals with the goal to exercise the influence of communications media for better access to information, education and knowledge and act as a stimulant for development. The premise for SODEC-Nepal’s involvement in development communication is based on the concept of multiplicity; democratization and participation at all levels by stressing the cultural and political identities of the local communities. </p><br/>
			  <p style="line-height: 24px; text-align:justify;">With strong assertion that “one is no longer attempting to create a need for the information one is disseminating, but one is rather disseminating information for which there is a need”, SODEC-Nepal produces and disseminates media that promotes good governance and accountability through the encouragement of citizen voices in media to promote the people's participation in development process that contributes to blend the authority and citizens in the governance system. In order to foster the process of empowering in grass roots level, SODEC-Nepal collaborates and works closely with media organizations, civil society organizations (CSOs), universities, UN agencies, embassies, and the Government of Nepal. </p><br/>
			  <p style="line-height: 24px; text-align:justify;">SODEC-Nepal mainly produces and broadcasts media contents for Radio and Television and is attempting to venture into cross-media platform to engage diverse development partners.</p>

