              <h3><font color="blue">Analyze the Existing Journalism Curricula of Nepali University</font></h3>

              <p>There has been significant qualitative and quantitative development of journalism studies in Nepal since its
                inception by Tribhuvan University on 1976. Gradually but eventually Kathmandu University and Purvanchal University
                followed suit, further enriching the advancement of journalism academia. However, there have been criticisms
                that the journalism curricula have been building a huge gap between the classroom instructions and newsroom
                requirements. </p>

              <a class="main-button more" href="news3.html">Show More <i class="fa fa-angle-right"></i></a>
              <div class="more_text">


                <p>The model curriculum by United Nations Educational, Scientific and Cultural Organization (UNESCO) in 2007
                  has detailed basics of what is required in journalism education. This project compared the journalism curricula
                  from all three of the mentioned universities and analyzed it based on the model curriculum by UNESCO. For
                  this research, there was extensive literature review with clearly described methodological tools. The outcome
                  of this research has been publicized through a book publication titled “Nepali Bishwabidhyalaya haruka
                  patrakarita Pathyakram” (Journalism curricula of Nepali universities). </p>

                <p>The project was facilitated by United Nations Educational, Scientific and Cultural Organization (UNESCO)
                  which was formally completed on 26th November 2014.</p>

