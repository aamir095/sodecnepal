              <h3><font color="blue">GEF-SGP through Media Lens</font></h3>
              <p>Global Environment Facility's – Small Grants Programme (GEF-SGP) was initiated by UNDP since 1992 in Nepal.
                Since then, more than 100 projects have been implemented in the area of biodiversity, climate change, land
                degradation and capacity building sectors to bring notable changes in livelihood of common people. </p>
              <a class="main-button more" href="news3.html">Show More <i class="fa fa-angle-right"></i></a>
              <div class="more_text">

                <p>People from different parts of the country were keenly interested to participate in this kind of public hearing
                  and interaction programs. The production team realized that there were many issues related to biodiversity,
                  climate change mitigation and land degradation. The program was able to find out about the improvement
                  in livelihood of local people, income generation and positive impacts by the preservation and conservation
                  of nature. </p>

                <p>Based on the achievements, successes and lesson learned from the projects implemented, Development Communication
                  Society Nepal (SODEC-Nepal) carried out production and broadcasting of the media activities which includes
                  video documentary, project based field report, studio based TV talk shows and public hearing. All of these
                  activities were based on diverse dynamics of the projects that are funded by SGP and implemented by different
                  community based organizations (CBOs) and non- governmental organizations (NGOs). </p>
                <p>The program has covered major areas of Nepal which included districts like Chitwan, Dang, Kathmandu, Kailali,
                  Kapilbastu, Lalitpur, Makwanpur and Syangja. Different concerned people and indigenous people related to
                  the subject were focused in the program. Participants of the program included both male and female. The
                  program was useful in highlighting the issues such as bio-diversity, climate change mitigation and land
                  degradation around the country. The qualitative and quantitative research was carried out during this process.
                  The issue covered in the program is important because it highlighted the achievements of local people.
                  Television programme production team closely consulted with SGP Nepal Office, implementing agencies and
                  local communities i.e. beneficiaries. We tried to maximize the participation of various people in the TV
                  programmes through vox-pops, bytes and interviews. We were careful to make the participation inclusive
                  from gender, ethnicity and other perspective. </p>
                <p>The project was facilitated GEF-SGP which was formally completed on 5th May 2015. </p>

