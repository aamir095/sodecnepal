              <p>The FP2020 global partnership is an outcome of the 2012 London Summit on Family Planning, where more than 20
                governments made a commitment to make affordable, lifesaving contraceptives, information, services and supplies
                available to 120 million women and girls in 69 of the world’s poorest countries by 2020. The movement now
                has over 80 commitments from governments, NGOs and other bodies. </p>

