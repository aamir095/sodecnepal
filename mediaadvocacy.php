              <h3><font color="blue">Analyzing the National Media Safety Landscape for Journalists</font></h3>

              <p>The safety of journalists is considered an important human rights issue in itself. Moreover, it is considered
                central to the realization of freedom of expression in broader context. The issue of journalists’ safety
                is critical to their being active agents in social and public life for the promotion of freedom of expression.
                If journalists cannot exercise the freedom of expression in safety, any provision that guarantees such freedom
                is of little value. In fact, ensuring journalists' safety is one of the significant aspects of vibrant democracy.
              </p>

              <a class="main-button more" href="news3.html">Show More <i class="fa fa-angle-right"></i></a>
              <div class="more_text">
                <p>s the United Nations specialized agency with a mandate to 'promote the free flow of ideas by word and image',
                  the United Nations Educational, Scientific and Cultural Organization (UNESCO) has been an important player
                  in the promotion of the safety of journalists and the fight against impunity. Over the years, it has been
                  monitoring the safety of journalists and state of impunity, and has published reports about this on a regular
                  basis. It has put the focus on the issue of journalists’ safety through various declarations, resolutions,
                  reports, and activities. </p>
                <p>This final report presents an assessment of the national media safety landscape for journalists in Nepal.
                  The study has employed the Journalists' Safety Indicators endorsed by the UN Plan of Action on the Safety
                  of Journalists and the Issue of Impunity. </p>

                <p>Employing the Journalists' Safety Indicators (JSI) endorsed by the UN Plan of Action on the Safety of Journalists
                  and the Issue of Impunity, a study has been conducted for an assessment of the national media safety landscape
                  for journalists in Nepal. Based on the assessment, a set of key-recommendations for each of the key indicator
                  categories of the JSI has been presented in addition to the report. </p>
                <p>Though there is a huge growth in terms of quantity in the media sector journalism remains professionally
                  weak and economically insecure, and the journalists are in highly vulnerable condition in terms of physical
                  and psychological safety. Nearly half of the journalists do not have any appointment letter/contract from
                  their employers, and journalism profession in Nepal is characterized by low wages, irregular payments,
                  poor working conditions, and declining credibility among the public. Journalists perceive that they are
                  prone to be victimized by both State and non-State actors, and the prolonged political transition has further
                  complicated their security situation.
                </p>
                <p>The status of women journalists is even more complicated. They are more vulnerable than their male colleagues
                  in terms of professional as well as psychological and physical safety. It is widely acknowledged that women
                  journalists have been facing various problems including exclusion, glass ceiling, gender pay gap, and harassment,
                  and they are more vulnerable than their male counterparts. They are particularly at risk from sexual harassment
                  even within media organisations, and there is a culture of impunity around sexual harassment.</p>
                <p>Impunity has been very serious concern of the stakeholders addressing journalists’ safety in Nepal. Some
                  media rights activists even fear of systematic impunity. As prompt, independent and efficient investigations
                  of crime against journalists have not been ensured, and prosecutions for violence and intimidation have
                  not been carried out against full chain of actors in violence and threats against journalists, the faith
                  on State agencies including the criminal and civil justice system is diminishing. Prevailing impunity in
                  Nepal poses serious concern with regard to freedom of expression and press freedom as journalists are practising
                  self-censorship due to impunity.</p>
                <p>The project was facilitated by United Nations Educational, Scientific and Cultural Organization (UNESCO)
                  which was formally completed on 28th July 2015.
                </p>

